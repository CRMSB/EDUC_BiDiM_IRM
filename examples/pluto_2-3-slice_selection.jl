		fig = Figure()
		ax = Axis(fig[1,1])
		for t in Trf
			res = t .* sinc.(pi.* f.* t)
			lines!(ax,f,res,label = "Trf = $t")
		end
		axislegend(ax)
		return fig
	end
end

