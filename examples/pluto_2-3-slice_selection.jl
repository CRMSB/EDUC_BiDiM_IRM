md"""
# Properties of a RF pulse

A RF pulse is characterized by its:
- Amplitude / $B_1$ envelop
- Bandwidth / duration / Time-Bandwidth Product
- Phase / Frequency Modulation

### Amplitude (B₁(t))

The strength of the RF magnetic field, usually measured in microtesla ($\mu\text{T}$). Its peak value and overall envelope shape dictate how much torque is exerted on the net magnetization vector.

For non-adiabatic pulse, the flip angle (in radian) can be calculated as :

$$\alpha = \gamma \int_{0}^{T} B_1(t) \, dt$$

Sometimes we use a version with the normalized shape envelope integral ($K_{shape}$) of the pulse and the $B_{1,\text{max}}$ in order to calculate the flip angle :

$$\alpha = \gamma \cdot B_{1,\text{max}} \cdot \Delta T \cdot K_{\text{shape}}$$

where $K_{\text{shape}} = \int_{0}^{T} b_1(t) \, dt$ and $b_1(t) = \frac{B_1(t)}{B_{1,\text{max}}}$.

### Bandwidth ($\Delta f$)
The range of frequencies excited by the pulse. In the presence of a magnetic field gradient ($G_z$), this frequency bandwidth maps directly to a physical spatial location and determines the slice thickness ($\Delta z$):

$$\Delta z = \frac{2\pi \cdot \Delta f}{\gamma \cdot G_z} = \frac{\Delta f}{\bar{\gamma} \cdot G_z}$$

For standard shapes, duration and bandwidth are locked together. Increasing the duration of the pulse reduces its bandwidth, making the slice narrower for a fixed gradient amplitude. Additionally, $B_{1,\text{max}}$ is also reduced when you increase the duration for a fixed target flip angle.

Two main metrics are used to characterize the bandwidth selectivity of an RF pulse:

#### Full Width at Half Maximum (FWHM)
The frequency bandwidth $\Delta f_{\text{FWHM}}$ is measured at $50\%$ of the peak excitation amplitude in the frequency domain. Under the **Small Flip Angle** approximation (where $M_{xy}(f) \propto \mathcal{F}\{B_1(t)\}$), it is computed directly from the Fourier Transform of the RF envelope:

$$\text{FWHM} = \frac{K_{\text{BW}}}{T}$$

where $K_{\text{BW}}$ is a dimensionless shape factor dependent on the pulse envelope profile (e.g., $K_{\text{BW}} \approx 1.21$ for a standard 3-lobe Sinc pulse, or $1.00$ for a rectangular pulse).

#### Time-Bandwidth Product (TBP or TBWP)
The **Time-Bandwidth Product** is a dimensionless figure of merit defined as the product of the total pulse duration $T$ and its frequency bandwidth $\Delta f$:

$$\text{TBP} = T \cdot \Delta f$$

* **Selectivity:** A higher TBP results in a sharper slice profile with steeper edges and less out-of-slice excitation (at the cost of requiring longer duration $T$ or higher peak $B_{1,\text{max}}$).
* **Shape dependence:** Standard Hamming-windowed Sinc pulses typically have $\text{TBP} \approx 4$, whereas SLR (Shinnar-Le Roux) designed pulses or high-performance multilobe pulses can achieve $\text{TBP} \approx 6 - 12+$.

---

> **Keep in mind that these are approximations!**
> 
> 1. **Linearity Breakdowns:** The Small Tip Angle approximation assumes $\sin(\alpha) \approx \alpha$. It works exceptionally well for $\alpha \le 30^\circ$, but breaks down severely for large tip angles ($90^\circ$ excitation or $180^\circ$ refocusing pulses) due to the non-linear nature of the Bloch equations. For large tip angles, **SLR algorithms** or numerical Bloch simulations are necessary to accurately predict the excitation profile.
> 2. **Truncation & Windowing:** Ideal Sinc waveforms require infinite duration. Truncating the pulse to a finite duration $T$ introduces ripples in the frequency domain (Gibbs phenomenon), which alters the true FWHM and effective TBP.


### Phase / Frequency Modulation

In general, an RF pulse is a complex-valued quantity:

$$B_1(t) = |B_1(t)| \cdot e^{i \phi(t)}$$

where the instantaneous frequency modulation $\Delta f(t)$ is the time derivative of the phase profile:

$$\Delta f(t) = \frac{1}{2\pi} \frac{d\phi(t)}{dt}$$

Modulating the phase $\phi(t)$ or frequency $\Delta f(t)$ serves four main purposes in MRI sequence design:

1. **Slice Offset / Spatial Positioning:**  
   Applying a constant carrier frequency offset $\Delta f_{\text{offset}}$ shifts the slice location along the gradient axis $z$ by a distance $\Delta z$:
   $$\Delta f_{\text{offset}} = \bar{\gamma} \cdot G_z \cdot \Delta z \implies \phi(t) = 2\pi \cdot \Delta f_{\text{offset}} \cdot t$$

2. **RF Phase Cycling:**  
   Applying a constant phase offset $\phi(t) = \phi_0$ controls the axis of rotation in the $x$-$y$ plane ($+x, +y, -x, -y$). This is fundamental for **spoiling** (e.g., RF spoiled GRE), **phase cycling** (e.g., SSFP), and artifact reduction schemes.

3. **Chirp / Frequency Sweeps & Adiabatic Pulses:**  
   Sweeping the instantaneous frequency across time $\Delta f(t)$ (e.g., linear frequency sweep $\phi(t) = \pi k t^2$) spreads the excitation over time. 
   * **Adiabatic Pulses** (e.g., BIR-4, Hyperbolic Secant) use synchronized amplitude and frequency sweeps to produce uniform flip angles ($\alpha$) that are immune to $B_1^+$ inhomogeneities above a minimum power threshold.
   * **Chirp Pulses** reduce the peak power ($B_{1,\text{max}}$) required to achieve a broad bandwidth $TBP$.
"""

