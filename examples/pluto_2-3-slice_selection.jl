# Small Angle approximation

In order to simulate a slice selective excitation we need to simulate both :
- rotation due to $B_1$
- linear frequency distribution generated by gradient waveform

In that specific case, the bloch equation has this form : 

$$\begin{equation}\begin{split}
\frac{d\vec{M}_{rot}}{dt} =&  \vec{M}_{rot} \times \gamma \vec{B}_{eff}\\
								=& \begin{bmatrix} M_x \\ M_y \\ M_z \end{bmatrix} \times 
					\begin{bmatrix} \gamma B_1^e(t) \cos(\theta) \\ -\gamma B_1^e(t) \sin(\theta) \\ \gamma \vec{G}.\vec{r} \end{bmatrix} 
\end{split}
\end{equation}$$



If you take a look at the bloch equation you see that in that case the we obtain a complex system of coupled ordinary differential equation that can't be solved analytically.

"""

