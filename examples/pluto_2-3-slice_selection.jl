    t_rf_start = 2.0
    t_rf_end   = 6.0
    t_rf_center = (t_rf_start + t_rf_end) / 2
    duration_rf = t_rf_end - t_rf_start

    t_ref_start = 6.2
    t_ref_end   = 8.2

    # --- RF Pulse (Hamming-Windowed Sinc) ---
    rf_envelope = zeros(length(t))
    rf_phase = zeros(length(t))

    for (i, ti) in enumerate(t)
        if t_rf_start <= ti <= t_rf_end
            # Normalized time from -1 to 1 across the pulse duration
            tau = (ti - t_rf_center) / (duration_rf / 2)
            # Sinc waveform with 3 lobes (zero crossings at tau = ±0.33, ±0.66)
            sinc_val = sin(3π * tau + 1e-9) / (3π * tau + 1e-9)
            # Hamming window to smooth truncation
            window = 0.54 + 0.46 * cos(π * tau)
            
            rf_envelope[i] = sinc_val * window
        end
    end

    # --- Z-Gradient Waveform (Slice Selection + Refocusing Lobe) ---
    gz = zeros(length(t))
