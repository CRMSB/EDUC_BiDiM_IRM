## Create an RF : RECT
In order to simulate the effect of the RF pulse we will simulate isochromate at each z position and then plot the magnetization at the end of the RF.

First let's create a RECT function that takes as input :
- an expected flip angle (in radian)
- a duration
- the timestep used for simulation

```julia
function create_rect(Trf=0.5e-3,alpha = pi/2; dt = 1e-6)
```

and that returns the B1 envelopped in radian

**Note :** Here our pulse is already in radian (because our rotation operator are expecting it). If we want to go back to Tesla we can divide by the gyromagnetic ratio $\gamma$.
"""

# ╔═╡ acd24a2f-4806-44a8-9444-d19e913edbe5
function create_rect(Trf=0.5e-3,alpha = pi/2; dt = 1e-6)
		rect_rf = ones(Float64,length(0:dt:Trf))
		rect_rf = rect_rf / (sum(rect_rf)) * alpha

		return rect_rf
end

